#!/usr/bin/env node
// Generates web/src/lib/api/connector-schemas.ts from openapi.json.
//
// Reads components.schemas.ConnectorConfig.oneOf[] and emits per-kind
// zod schemas + field metadata. Run as part of `pnpm openapi`.

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "../..");
const openapiPath = resolve(repoRoot, "openapi.json");
const outPath = resolve(__dirname, "../src/lib/api/connector-schemas.ts");

const spec = JSON.parse(readFileSync(openapiPath, "utf8"));
const variants = spec.components?.schemas?.ConnectorConfig?.oneOf;
if (!Array.isArray(variants)) {
  throw new Error("ConnectorConfig.oneOf not found in openapi.json");
}

function quote(s) {
  return JSON.stringify(s);
}

function buildVariant(variant) {
  const kind = variant.properties?.kind?.enum?.[0];
  if (!kind) throw new Error("variant missing kind discriminator");

  const required = new Set(variant.required ?? []);
  const props = variant.properties ?? {};

  // Sort: required first, then optional. Within each group, alphabetical.
  const propEntries = Object.entries(props)
    .filter(([name]) => name !== "kind")
    .sort(([a, pa], [b, pb]) => {
      const ra = required.has(a) ? 0 : 1;
      const rb = required.has(b) ? 0 : 1;
      return ra - rb || a.localeCompare(b);
    });

  const fields = [];
  const zodLines = [`      kind: z.literal(${quote(kind)})`];

  for (const [name, prop] of propEntries) {
    const isRequired = required.has(name);
    const isSecret = prop.format === "password";
    const description = prop.description ?? "";
    // Multiline heuristic: JSON blobs / PEM / certificates / *_json fields.
    const multiline =
      /JSON|PEM|certificate/i.test(description) || name.endsWith("_json");

    fields.push({
      name,
      description,
      example: prop.example,
      required: isRequired,
      secret: isSecret,
      multiline,
    });

    if (isRequired) {
      // Strip trailing period from description for cleaner error message.
      const label = (description || name).replace(/\.$/, "");
      const msg = `${label} is required`;
      zodLines.push(`      ${name}: z.string().min(1, ${quote(msg)})`);
    } else {
      zodLines.push(
        `      ${name}: z.string().optional().transform((v) => (v === "" ? undefined : v))`,
      );
    }
  }

  return { kind, fields, zodBody: zodLines.join(",\n") };
}

const built = variants.map(buildVariant);

const header = `// AUTO-GENERATED by scripts/extract-connector-schemas.mjs.
// Do not edit by hand — re-run \`pnpm openapi\` to regenerate.
//
// Source of truth: server/src/connectors/config.rs (ConnectorConfig enum
// with #[derive(utoipa::ToSchema)]).

import { z } from "zod";

export interface FieldMeta {
  name: string;
  description: string;
  example?: string;
  required: boolean;
  secret: boolean;
  multiline: boolean;
}

export type ConnectorKind = ${built.map((b) => quote(b.kind)).join(" | ")};

`;

const entries = built
  .map(
    (b) => `  ${quote(b.kind)}: {
    zod: z.object({
${b.zodBody},
    }),
    fields: ${JSON.stringify(b.fields, null, 2).replace(/\n/g, "\n    ")} satisfies FieldMeta[],
  }`,
  )
  .join(",\n");

const out = `${header}export const connectorSchemas = {
${entries},
} as const;

export type ConnectorFormValues<K extends ConnectorKind = ConnectorKind> = z.input<
  (typeof connectorSchemas)[K]["zod"]
>;
`;

writeFileSync(outPath, out, "utf8");
console.log(`✓ connector-schemas.ts written (${built.length} kinds)`);
